import random
print("Random module loaded")

print(random.random())
# This is a simple Python module that imports the random module and prints a random float.

print(random.randint(1, 10))
print("Random integer between 1 and 10 generated")

print(random.uniform(1.5,3.5))
print("Random float between 1.5 and 3.5 generated")
# This module demonstrates the use of the random module to generate random numbers.

print(random.choice(['apple', 'banana', 'cherry']))
print("Random choice from a list generated")